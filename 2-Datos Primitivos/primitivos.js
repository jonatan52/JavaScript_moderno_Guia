//Primitivos: Es una informacion que no es un objeto y son inmutables
/*- Symbol : Valor unico que no es igual a otro valor
*/

// String : cadena de texto
let nombre = 'jonatan tarriba';
console.log(nombre);

nombre = "bryan tarriba"
console.log(nombre);

console.log(typeof nombre);

//  Bolean : true / false
let esMarvel = false;
console.log( typeof esMarvel)

//Number : integer, float, etc
let edad = 36;
console.log( typeof edad );

let grados = 36.5;
console.log( typeof grados);

// undefined : Variable declarada que aun no se le asignado valor
let variableSinDefinir;
console.log( typeof variableSinDefinir);

// null : Sin valor en lo absoluto
let soyNull = null;
console.log(typeof soyNull);
